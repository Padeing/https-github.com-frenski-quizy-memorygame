<h1 class="font-mono text-2xl text-red-800">What Is the Past Perfect Tense? (with Examples)</h1>
<p>The past perfect tense describes a completed activity in the past. It is used to emphasize that an action was completed before another action took place. For example:
<li>John had baked a cake before you arrived.</li>
<li>They had painted the fence before I had a chance to speak to them.</li>
<br>
<h3 class="text-purple-500 font-serif text-lg">More Examples of the Past Perfect Tense</h3>
Here are some more examples of the past perfect tense (shaded):
<li>Silverfinger had taken the pill before the team reached him.
(First: He took the pill. Next: The team reached him.)</li>
<li>I had called the police before I investigated the noise in the garden.
(First: I called the police. Next: I investigated the noise.)</li>
<li>The weather changed, but the team had planned its next move.
(First: The team planned. Next: The weather changed.)</li> <br>
<h2 class="text-xl font-serif text-cyan-700">Forming the Past Perfect Tense</h2>
<h3 class="text-purple-500 font-serif text-lg">The past perfect tense is formed:</h3>
"had" + [past participle]
I had jumped <br>
I had met
<br>
<br>
<h2 class="text-xl font-serif text-cyan-700">Forming the Past Participle (Regular Verbs)</h2>
If it's a regular verb, the past participle is the same as the simple past tense. In other words, it is formed like this:

<h3 class="text-purple-500 font-serif text-lg">Add "ed" to most verbs:</h3>
jump > jumped <br>
paint > painted

<h3 class="text-purple-500 font-serif text-lg">If a verb of one syllable ends [consonant-vowel-consonant], double the final consonant and add "ed":</h3>
chat > chatted <br>
stop > stopped

<h3 class="text-purple-500 font-serif text-lg">If the final consonant is "w," "x" or "y," don't double it:</h3>
sew > sewed <br>
play > played <br>
fix > fixed

<h3 class="text-purple-500 font-serif text-lg">If last syllable of a longer verb is stressed and ends [consonant-vowel-consonant], double the last consonant and add "ed":</h3>
incur > incurred <br>
prefer > preferred

<h3 class="text-purple-500 font-serif text-lg">If the first syllable of a longer verb is stressed and the verb ends [consonant-vowel-consonant], just add "ed":</h3>
open > opened <br>
enter > entered <br>
swallow > swallowed

<h3 class="text-purple-500 font-serif text-lg">If the verb ends "e," just add "d":</h3>
thrive > thrived <br>
guzzle > guzzled

<h3 class="text-purple-500 font-serif text-lg">If the verb ends [consonant + "y"], change the "y" to an "i" and add "ed":</h3>
cry > cried <br>
fry > fried 
<br>
<br>
<h2 class="text-xl font-serif text-cyan-700">Forming the Past Participle (Irregular Verbs)</h2>
If it's an irregular verb, the past participle is formed in all sorts of different ways. Here are some examples:
arise > arisen <br>
catch > caught <br>
choose > chosen <br>
know > known <br>
You just have to learn them.

<br>
<br>
<h2 class="text-xl font-serif text-cyan-700">The Negative Version</h2>
If you need the negative version, you can use the following construction:
"had not" + [past participle]
<li>Silverfinger had not taken the pill before the team reached him.</li>
<li>I had not called the police before I investigated the noise in the garden.</li>
<li>The weather changed, and the team had not planned its next move.</li>
<li>Remember that "had not" is sometimes written as the contraction "hadn't."</li>
<br>
<br>
<h2 class="text-xl font-serif text-cyan-700">The Question Version</h2>
If you need to ask a question, you can use the following word order for a yes/no question:
"had" + [subject] + [past participle]
<li>Had Silverfinger taken the pill before the team reached him?</li>
<li>Had the team planned its next move before the weather changed?</li>
<br>
<h2 class="text-xl font-serif text-cyan-700">You can use the following word order for a question-word question:</h2>
[question word] + "had" + [subject] + [past participle]
<li>Why had Silverfinger taken the pill before the team reached him?</li>
<li>Where had the team planned its next move before the weather changed?</li>
<br>
<h2 class="text-xl font-serif text-cyan-700">Using Contractions</h2>
Don't forget that in speech and writing (especially informal writing), you will encounter the following contractions:
I had > I'd
You had > You'd
He had > He'd
She had > She'd
It had > It'd
We had > We'd
They had > They'd
Also, for the negative version, you will commonly see "hadn't" instead of "had not."</p>


