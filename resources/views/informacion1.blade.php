<h1 class="font-mono text-2xl text-red-800">What Is the Simple Past Tense? (with Examples)</h1>
<p>The simple past tense is used to describe a completed activity that happened in the past. In other words, it started in the past and ended in the past. For example:</p>
<li>John baked a cake.</li>
<li>They painted the fence.</li>
<br>

<h2 class="text-xl font-serif text-cyan-700">Real-life Examples of the Simple Past Tense</h2>
<li>Here are some real-life examples of the simple past tense:
<li>I saw the angel in the marble and carved until I set him free. (Italian sculptor Michelangelo)
<li>I asked God for a bike, but I know God doesn't work that way so I stole a bike and asked for forgiveness.
<br>
<br>
<h2 class="text-xl font-serif text-cyan-700">Forming the Simple Past Tense</h2>
<p>If you're dealing with a regular verb, the simple past tense is formed like this:
base form of verb + "ed"
jump > jumped <br>
paint > painted <br>
However, there are some spelling rules.</p>
<br>
<h2 class="text-xl font-serif text-cyan-700">Spelling Rules</h2>

<p><h3 class="text-purple-500 font-serif text-lg">If a verb of one syllable ends [consonant-vowel-consonant], double the final consonant and add "ed":</h3>
chat > chatted <br>
stop > stopped 
<br>
<br>
<h3 class="text-purple-500 font-serif text-lg">If the final consonant is w, x, or y, don't double it:<h3>
sew > sewed <br>
play > played <br>
fix > fixed
<br>
<br>
<h3 class="text-purple-500 font-serif text-lg">If last syllable of a longer verb is stressed and ends [consonant-vowel-consonant], double the last consonant and add "ed":</h3>
incur > incurred <br>
prefer > preferred
<br>
<br>
<h3 class="text-purple-500 font-serif text-lg">If the first syllable of a longer verb is stressed and the verb ends [consonant-vowel-consonant], just add "ed":</h3>
open > opened <br>
enter > entered <br>
swallow > swallowed
<br>
<br>
<h3 class="text-purple-500 font-serif text-lg">If the verb ends "e", just add "d":</h3>
<p>thrive > thrived <br>
guzzle > guzzled
If the verb ends [consonant + "y"], change the "y" to an "i" and add "ed":
cry > cried <br>
fry > fried</p>
<br>
<h2 class="text-xl font-serif text-cyan-700">Forming the Simple Past Tense of Irregular Verbs</h2>
<p>If it's an irregular verb, the simple past tense is formed in all sorts of different ways. Here are some examples:
<br>
break > broke <br>
catch > caught <br>
find > found <br>
see > saw</p>
You just have to learn them.
<br>
<br>
<h2 class="text-xl font-serif text-cyan-700">Using the Simple Past Tense</h2>
When making a statement, you can use the following word order:
[subject] + [verb]
<li>The Martians landed near the aqueduct.</li>
<li>The burglar used the fire escape.</li>
<br>

<h2 class="text-xl font-serif text-cyan-700">The Negative Version</h2>
If you need the negative version, you can use the following word order:
"did not" + base form of verb
<li>The Martians did not land near the aqueduct.</li>
(We could have used "didn't" instead of "did not.")
<li>The burglar did not use the fire escape.</li>
<br>

<h2 class="text-xl font-serif text-cyan-700">The Question Version</h2>
If you need to ask a question, you can use the following word order for a yes/no question:
"did" + [subject] + base form of verb
<li>Did the Martians land near the aqueduct?</li>
<li>Did the burglar use the fire escape?</li>
<br>

<h2 class="text-xl font-serif text-cyan-700">You can use the following word order for a question-word question:</h2>
<p>[question word] + "did" + [subject] + base form of verb
<li>Why did the Martians land near the aqueduct?</li>
<li>When did the burglar use the fire escape?</li>
<br>

<h2 class="text-xl font-serif text-cyan-700">You can use the following word order for a choice question:</h2>
"did" + [subject] + base form of verb + choice A + or + choice B
<li>Did the Martians land near the aqueduct or the town?</li>
<li>Did the burglar use the fire escape or the stairs?</li></p>
